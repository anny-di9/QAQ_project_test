package classArchitectureTraining;


import java.util.List;

class Candidate {
    String firstName;
    String lastName;
    int age;
    List<String> skills;

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }

    public List<String> getSkills() {
        return skills;
    }
}